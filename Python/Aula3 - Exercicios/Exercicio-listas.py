L = [5, 7, 2, 9, 4, 1, 3]
#A) Tamanho da Lista
print(len(L), "Comprimento da lista")
#B) maior valor da lista
print(max(L), "Maior numero da lista")
#C) menor valor da lista
print(min(L), "Menor numero da lista")
#D) soma de tdos os elementos
print(sum(L), "Soma de todos os elementos")
#E) lista em ordem crescente
print(sorted(L), "Ordem Crescente")
#F) ordem decrescente
L.sort(reverse=True)
print(L, "Ordem Decrescente")